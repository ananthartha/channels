package channels

import (
	"testing"
)

func TestQueueChannelInputAndOutput(t *testing.T) {
	q := NewQueueChannel[int](NewDefaultQueue[int]())

	if q.Len() != 0 {
		t.Error("empty queue length not 0")
	}

	for i := 0; i < 1000; i++ {
		q.In() <- i
		if q.Len() != i+1 {
			t.Error("adding: queue with", i, "elements has length", q.Len())
		}
	}
	for i := 0; i < 1000; i++ {
		<-q.Out() // Read the element from Q
		if q.Len() != 1000-i-1 {
			t.Error("removing: queue with", 1000-i-i, "elements has length", q.Len())
		}
	}
}
